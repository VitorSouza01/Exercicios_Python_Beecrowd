"""
Leia quatro valores inteiros A, B, C e D. A seguir, calcule e mostre a diferença do produto de A e B pelo produto de C
e D segundo a fórmula: DIFERENCA = (A * B - C * D).
"""

A = float(input())
B = float(input())
C = float(input())
D = float(input())

DIFERENCA = (A * B - C * D)

print("DIFERENCA = %i" % DIFERENCA)
